`timescale 1ns / 1ps

// =========================================================================
// 模块名称：imm_gen (立即数生成单元 Immediate Generator)
// 功能描述：根据指令和立即数类型编码，提取指令中的立即数并进行符号扩展或移位，
//          生成统一的 32 位立即数输出。
// =========================================================================
module imm_gen (
    input  wire [31:0] inst,       // 32 位机器指令
    input  wire [2:0]  imm_type,   // 立即数类型控制
    output reg  [31:0] imm         // 32 位立即数输出
);

    always @(*) begin
        case (imm_type)
            // 0: 12 位符号扩展立即数 (addi.w, ld.w, st.w 等)
            3'd0: imm = {{20{inst[21]}}, inst[21:10]};

            // 1: 20 位高位移位立即数 (lu12i.w: 将 20 位立即数左移 12 位)
            3'd1: imm = {inst[24:5], 12'b0};

            // 2: 16 位分支偏移立即数 (beq, bne, jirl 等: 左移 2 位后符号扩展)
            3'd2: imm = {{14{inst[25]}}, inst[25:10], 2'b00};

            // 3: 26 位无条件跳转偏移立即数 (b: 拆分拼接后左移 2 位符号扩展)
            3'd3: imm = {{4{inst[9]}}, inst[9:0], inst[25:10], 2'b00};

            default: imm = 32'd0;
        endcase
    end

endmodule
